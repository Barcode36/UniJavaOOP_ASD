package ESERCIZI_ASD_SECONDO_SEMESTRE.ESERCIZIO_16;

/**
 * Il contenuto di ogni nodo deve essere rappresentato nel formato "chiave:valore"
 */
public class BSTNode {

    int key;
    String value;
    BSTNode left, right, parent;

    BSTNode(int key, String value) {
        this.key = key;
        this.value = value;
        left = null;
        right = null;
        left = null;
    }

}

